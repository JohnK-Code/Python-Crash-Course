# Adding whitespace to strings

print("Python") # prints a standard string

print("\tPython") # adds a tab at the begining of the string using '\t' - four spaces before the word 'Python' is printed.

print("Languages:\nPython\nC\nJavaScrpt") # adds a newline to the string using '\n' - This can also be combined with '\t' to add a tab to the string aswell - example: '\n\t' would add a new line and a tab

