# underscores in numbers - chapter 2

universe_age = 14_000_000_000 # underscores can be used in long numbers to make them more readable 
print(universe_age) # equals 14000000000 - underscores are ignored 

# underscores only works in python 3.6 or newer
# the underscore can be anywhere in number - 3_000 same as 30_00