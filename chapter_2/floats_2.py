# floats exercise form chapter 2

print(0.1 + 0.1)
print(0.2 + 0.2)
print(2 * 0.1)
print(2 * 0.2)

# some multiplications using floats end with an extra number of decimal places - not a problem don't worry about it 
print(0.2 + 0.1) # equals 0.30000000000000004
print(3 * 0.1) # equals 0.30000000000000004

# if you divide two numbers, even whole integers and the answer is a whole number it is still displayed as a float
print(4/2) # equals 2.0 not 2 - weird :-)

# if you mix integers and floats in a multiplication, you'll get a float as the answer
print(1 + 2.0) # equals 3.0
print(2 * 3.0) # equals 6.0
print(3.0 ** 2) # equals 9.0